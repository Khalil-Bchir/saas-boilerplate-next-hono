/**
 * Writes src/routes/manifest.ts with static import() loaders for each actions.ts
 * under src/routes (recursive). Run after adding a new route module.
 */
import { readdir, writeFile } from 'node:fs/promises'
import { dirname, join, relative } from 'node:path'
import { fileURLToPath } from 'node:url'

const root = join(dirname(fileURLToPath(import.meta.url)), '..')
const routesDir = join(root, 'src', 'routes')

/** @param {string} dir @param {string[]} acc */
async function collectActionFiles(dir, acc = []) {
  const entries = await readdir(dir, { withFileTypes: true })
  for (const e of entries) {
    const p = join(dir, e.name)
    if (e.isDirectory()) {
      await collectActionFiles(p, acc)
    } else if (e.isFile() && e.name === 'actions.ts' && !p.includes('manifest')) {
      acc.push(p)
    }
  }
  return acc
}

function toImportPath(absFile) {
  const rel = relative(join(root, 'src', 'routes'), absFile).replace(/\\/g, '/')
  const withoutExt = rel.replace(/\.ts$/, '.js')
  return `./${withoutExt}`
}

const files = (await collectActionFiles(routesDir)).sort()
const loaders = files.map((f) => `  () => import('${toImportPath(f)}'),`).join('\n')

const contents = `import type { AutoLoadRoute } from 'hono-autoload/types'

// Generated by scripts/generate-route-manifest.mjs
// Any file matching src/routes/**/actions.ts is included automatically.
export type RouteModuleLoader = () => Promise<{
  default?: AutoLoadRoute | AutoLoadRoute[]
}>

export const routeModuleLoaders: RouteModuleLoader[] = [
${loaders}
]
`

const out = join(routesDir, 'manifest.ts')
await writeFile(out, contents, 'utf8')
console.log(`Wrote ${out} (${files.length} route module(s))`)
